package com.example.vanphu.mymoney.model;


public class imageModel {
    private int mImage;
    private String mName;
    public imageModel(int mImage, String mName) {
        this.mImage = mImage;
        this.mName = mName;
    }
    public int getmImage() {
        return mImage;
    }
    public String getmName() {
        return mName;
    }

}

